    private void addDonorList()
    {
            try {
                if (conn != null) {
                    System.out.println("Connected");
                } 
             
                String sql = "select * from bloodDonor";
                PreparedStatement st = conn.prepareStatement(sql);
                ResultSet rs = st.executeQuery();
                ResultSetMetaData res= rs.getMetaData();
                
                while(rs.next())
                {
                    StringBuilder c= new StringBuilder();
                    c.append(rs.getString("bloodDonorId")+" ");
                    c.append(rs.getString("bloodDonorName")); 
//                    c.append(rs.getString("cityId")); 
                    donorRemove.addItem(c.toString()); 
                } 
                } 
            
            catch (Exception e) 
            {
                 System.out.println("Error in hospital add");
                 e.printStackTrace();
            }
    }
    
    private void removeDonor()
    {
            try {
                 
                if (conn != null) {
                    System.out.println("Connected");
                } 
                String id= (String) donorRemove.getSelectedItem();
                id= id.substring(0,id.indexOf(" "));
                System.out.println(id);
                String sql = "DELETE FROM bloodDonor WHERE bloodDonorId = "+id;
                Statement stmt = conn.createStatement();
                stmt.executeUpdate(sql); 
                } 
            
            catch (Exception e) 
            {
                 System.out.println("Error in donor delete");
                 e.printStackTrace();
            }
    }
     
    
